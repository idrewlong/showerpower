                <p class="mt-3 text-lg">Teresa Renkenberger is the founder of Shower Power. Mary Ann Kirby is the director of operations. Together they have created a safe place for the area's homeless and marginally sheltered to come and take a shower, be fed, clothed, and be loved.<br><br>
    
                    Every Friday, since late November of 2019, Shower Power offers a free shower to anyone who needs it in downtown Jackson. A shower truck is located at 836 South Commerce Street. It’s been retrofitted with two separate shower units, each with their own soap and shampoo dispensers, a mirror, a sink, and changing area. Water is fed through a regular garden hose and a standard water heater to provide for each shower.<br><br>
    
                    Since then, Shower Power has given well over 3,500 showers to people in need and fed, fellowshipped, and ministered to as many as a few hundred unique visitors. It’s estimated that the homeless population in Jackson numbers somewhere between 800 and 1,000. Food and basic necessities, clothing items, and hygiene items, are provided as well. <br><br>
    
                    In October of 2020, Project Hope, as an extension of Shower Power, was born. Project Hope serves to identify which of the unsheltered Shower Power visitors would be good candidates for low-income housing—and then helps them get there. They currently have eleven people in the program. For $9,000 per year, provided by corporate and individual sponsorships, housing and groceries are secured. True life-change comes with not having to sleep on the street. <br><br>
    
